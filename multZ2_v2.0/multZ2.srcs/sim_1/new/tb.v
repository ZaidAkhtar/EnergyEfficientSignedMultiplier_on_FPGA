module tb();
    reg [7:0] a;
    reg [7:0] b;
    wire [15:0]prodCSLA;
    wire [15:0]prodCompr;
    wire [15:0]prodSP;
    PartProds DUT(a,b,prodCSLA,prodCompr,prodSP);
    
    initial
    begin
        a = 8'b1111_0011; b = 8'b0000_1010;
        #5 a = 8'b0001_0011; b = 8'b0001_1010;
        #5 a = 8'b1110_0011; b = 8'b1111_1110;
        #5 a = 8'b0111_1111; b = 8'b0111_1111;
        #5 a = 8'b1100_1001; b = 8'b0000_0000;
        #5 $finish;
    end 
endmodule
